逐步了解手术可以揭开体验的神秘面纱。

**到达和准备（30分钟）：**
您将完成最终的文书工作和同意书。手术团队审查您的治疗计划并回答最后的问题。检查生命体征，如果您接受镇静，可能会放置静脉输液管。

**麻醉给药：**
大多数种植牙手术使用局部麻醉，与补牙使用的麻醉药物相同。您会感到压力但不会疼痛。对于焦虑的患者或复杂病例，选项包括：
- 仅局部麻醉（完全清醒，区域麻木）
- 局部麻醉+口服镇静（放松但有意识）
- 局部麻醉+静脉镇静（半意识，对手术记忆有限）
- 全身麻醉（完全无意识，医院环境）

